def continuous_peaks_ff(x, t):
    max0 = 0
    count = 0
    for x_i in x:
        if x_i == '0': 
            count += 1
        elif count > max0:
            max0 = count
            count = 0
    max1 = 0
    count = 0
    for x_i in x:
        if x_i == '1': 
            count += 1
        elif count > max1:
            max1 = count
            count = 0
    r = 0
    if max0 > t and max1 > t:
        r = len(x)
    return max(max0, max1) + r


